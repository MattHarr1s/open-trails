		<h1>Shawn's Use Case #1</h1>
		<p>When Shawn first uses the website, he is presented with an option to register an account. He may do so by linking a Facebook or Google+ account, or by providing an email address and setting a password.</p>
		<p>Shawn clicks the “link with Facebook account” button and is immediately brought to the homepage of the site.</p>
		<p>From here, Shawn clicks on the “Find a trail” button. He uses advanced search options to display only the trails listed as “high difficulty”. He then sorts these trails by length by clicking on the head of the “length” column.</p>
		<p>At this point, the system is displaying a list of “high difficulty” trails, the first of which is the longest. It is named “Dead Man’s Pass”.</p>
		<p>Shawn clicks on “Dead Man’s Pass” and is taken to a page containing a map of the trail and its current weather conditions.</p>
		<p>Shawn scrolls down on the “Dead Man’s Pass” page and begins to read comments left by other users. One reads:  “Beware, ye who tread here”. Shawn clicks the “reply” button located within this user’s comment box, which displays an input field. Shawn then types “Challenge accepted!” into it and submits it by clicking an “OK” button.</p>
		<p>Shawn continues to scroll down the page until he happens upon a comment which says “this trail sux!” Shawn clicks a tiny down arrow located within the comment box, which lowers its relevancy. This is reflected by a number located within the comment box and has now been decreased by 1, to -1. Because this comment now has the lowest rating of all the comments on the page, it immediately drops to the bottom of the page.</p>
		<p>Shawn then scrolls back to the top of the page and clicks a “+” which allows him to zoom in on the map. After studying it sufficiently he clicks the drop-down menu in the upper-right hand corner of the screen and selects the “sign out” option.</p>
